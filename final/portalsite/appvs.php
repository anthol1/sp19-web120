      <h2>Mobile Applications VS Responsive Design</h2>

      <div id="content">
         <div class="text-area">
            <p>There is a dilemma nowadays for developers where they have to chose to design an own application for mobile use, or stick to the earlier design but just design it responsive. </p>
         </div>
         <h3>Pros and cons with responsive websites:</h3>
         <div class="text-area">
            <p>Pros:<br>
            It takes less time to just implement a responsive design to your website than developing an app for it.
            <br>
            <br>
            It’s very much cheaper, because you just have to implement something into your existing design, rather than creating something new.
            </p>
         </div>

         <div class="text-area">
            <p>Cons:<br>
            There are cons however, and these cons sets up the plan for your design entirely. When it comes to the user experience, hosting a responsive website instead of a mobile app can be very slow, not user friendly and be hard to navigate. 
            </p>
         </div>


         <h3>Pros and cons with mobile applications:</h3>
         <div class="text-area">
         <p>Pros:<br>
            It’s usually way more faster to navigate an application than a website, at least when you’re on your handheld device. 

            The functionality is usually way more fitting in application format than website format.

         </p>

         </div>
         <div class="text-area">
            <p>Cons:<br>
               The cons are the counterpart of responsive website’s pros. It takes more time, usually more manpower (because another set of skills is required), and a bigger wallet.
            </p>


         </div>
         <h3>So what to choose?</h3>
         <p>This all depends on what you want to do. Mobile applications are not coded using HTML and CSS usually, and it requires another sets of skills from the developer.

Are you developing something for a bigger company that has an idea where mobile use is very important, then I suggest that you would get into mobile application development and use those skills to do that. Are you designing an informational platform that can be read on a handheld device? Then you probably need to just be sure the website is readable on a phone.
</p>
         </div>



